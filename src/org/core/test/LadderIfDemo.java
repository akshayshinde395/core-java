package org.core.test;

public class LadderIfDemo {
    public static void main(String[] args) {
        int number = 2;

        if (number == 0) {
            System.out.println("zero");
        } else if (number > 0) {
            System.out.println("+ve");
        } else {
            System.out.println("-ve");
        }
    }
}
